import asyncio

async def generate_curl_poc(vulnerability_data):
    """Generates an executable PoC command for the user."""
    url = vulnerability_data.get("url")
    method = vulnerability_data.get("method", "GET")
    params = vulnerability_data.get("params", "")
    
    poc = f"curl -X {method} '{url}'"
    if params:
        poc += f" -d '{params}'"
    
    return poc

